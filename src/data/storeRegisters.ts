/**
 * Дефиниции на дневниците (регистрите) по системата за самоконтрол
 * за МАГАЗИН ЗА ТЪРГОВИЯ С ХРАНИ БЕЗ ТОПЛА ТОЧКА.
 *
 * Всеки регистър се пази във Firestore на ключ:
 *   danka_logs_<sanitizedEmail>_reg_<registerId>_<period>
 * където period е "YYYY-MM" (месечни), "YYYY" (годишни) или "all" (постоянни).
 */

export type CellType = "text" | "number" | "time" | "date" | "check" | "select" | "temp";

export interface RegisterColumn {
  key: string;
  label: string;
  type: CellType;
  /** За type: "select" — фиксирани опции */
  options?: string[];
  /** Динамични опции от друг списък в системата */
  optionsFrom?: "suppliers" | "cleaningAgents" | "employees" | "menuProducts";
  placeholder?: string;
  /** За температури — допустима норма (за оцветяване при нарушение) */
  norm?: { min?: number; max?: number };
  /** Тесен вход (напр. числа, дати) */
  narrow?: boolean;
}

export type RegisterKind =
  | "rows"        // динамични редове (добавяне на запис)
  | "grid-days"   // фиксирани редове 1..31 за месеца
  | "grid-weeks"  // редове I..V седмица за месеца
  | "temp-units"  // температурни чек-листове по съоръжения
  | "training"    // протоколи за обучение
  | "survey"      // въпросник (Ликъртова скала 1–5)
  | "checklist3"; // чек-лист със скала 1–3 по служител

export type RegisterFrequency =
  | "daily" | "weekly" | "monthly" | "quarterly" | "yearly" | "delivery" | "event" | "permanent" | "3days";

export interface InfoPanel {
  title: string;
  items: string[];
}

export interface RegisterDef {
  id: string;
  /** Пореден номер за визуализация/печат */
  num: number;
  title: string;
  shortTitle: string;
  subtitle?: string;
  /** „Кога се попълва" — показва се като информация под/над таблицата */
  fillWhen: string;
  frequency: RegisterFrequency;
  /** Гранулярност на Firestore документа */
  period: "month" | "year" | "all";
  kind: RegisterKind;
  columns?: RegisterColumn[];
  legend?: string[];
  instructions?: string[];
  corrective?: string[];
  infoPanels?: InfoPanel[];
  /** Участва в ежедневните напомняния */
  remind?: boolean;
  /** За kind: "training" — план от теми за обучение (по подразбиране TRAINING_TOPICS) */
  trainingTopics?: string[];
}

